# Convertendo int para str
numero = 10
print(type(numero))
numero = str(numero)
print(type(numero))

# Convertendo int para float
numero = float(numero)
print(numero)
print(type(numero))

# Convertendo de str para int ou float
valor_str = '25.2'
valor_str2 = '25'
valor_int = int(valor_str2)
valor_float = float(valor_str)
print(valor_str, type(valor_str))
print(valor_int, type(valor_int))
print(valor_float, type(valor_float))
